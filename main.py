a = int(input("Введите целое число: ")) #инициализируем переменную а и просим ввести ее значение.
if a % 2 == 0: #условие через которое проверяется четность числа.
    print("Число является четным") #вывод если число четное.
else: #при невыполнении условия 2 строчки будет выполняться 5.
    print("Число является нечетным") #вывод если число нечетное.